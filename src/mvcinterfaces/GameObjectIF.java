package mvcinterfaces;

/**
 *  Interface for the GameObject.
 * @author florian
 */
public interface GameObjectIF {

    /**
     *  Gets the height.
     * @return height
     */
    double getHeight();

    /**
     *  Gets the output ID.
     * @return outputID
     */
    int getOutputID();

    /**
     *  Gets the width.
     * @return width
     */
    double getWidth();

    /**
     *  Gets the x position.
     * @return xPos
     */
    double getxPos();

    /**
     *  Gets the y position.    
     * @return yPos
     */
    double getyPos();
}
